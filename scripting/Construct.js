// Example constructor class
// This is a simple example of a constructor class. It is used to create a new object with a name and a color.
//
class Car {
  constructor(name, color) {
    this.name = name;
    this.color = color;
  }
}

// Create a new object
const car = new Car("Toyota", "black");
console.log(car);
// This class creates a new object with the name Toyota and the color black.
